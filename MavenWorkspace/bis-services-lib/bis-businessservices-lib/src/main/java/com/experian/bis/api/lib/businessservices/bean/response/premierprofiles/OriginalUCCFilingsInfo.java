package com.experian.bis.api.lib.businessservices.bean.response.premierprofiles;

public class OriginalUCCFilingsInfo {
	private String dateFiled;
	private String legalAction;
	private String documentNumber;
	private String filingState;
	public String getDateFiled() {
		return dateFiled;
	}
	public void setDateFiled(String dateFiled) {
		this.dateFiled = dateFiled;
	}
	public String getLegalAction() {
		return legalAction;
	}
	public void setLegalAction(String legalAction) {
		this.legalAction = legalAction;
	}
	public String getDocumentNumber() {
		return documentNumber;
	}
	public void setDocumentNumber(String documentNumber) {
		this.documentNumber = documentNumber;
	}
	public String getFilingState() {
		return filingState;
	}
	public void setFilingState(String filingState) {
		this.filingState = filingState;
	}
	
}
