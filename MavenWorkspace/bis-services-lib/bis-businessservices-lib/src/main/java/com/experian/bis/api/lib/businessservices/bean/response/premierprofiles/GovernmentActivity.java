package com.experian.bis.api.lib.businessservices.bean.response.premierprofiles;

public class GovernmentActivity {
	
	private String congressionalDistrict;
	private String contractor;
	private String debarredIndicator;
	private String agency;
	private String exclusionType;
	private String activeDate;
	private String terminationDate;
	private String importExportCode;
	public String getCongressionalDistrict() {
		return congressionalDistrict;
	}
	public void setCongressionalDistrict(String congressionalDistrict) {
		this.congressionalDistrict = congressionalDistrict;
	}
	public String getContractor() {
		return contractor;
	}
	public void setContractor(String contractor) {
		this.contractor = contractor;
	}
	public String getDebarredIndicator() {
		return debarredIndicator;
	}
	public void setDebarredIndicator(String debarredIndicator) {
		this.debarredIndicator = debarredIndicator;
	}
	public String getAgency() {
		return agency;
	}
	public void setAgency(String agency) {
		this.agency = agency;
	}
	public String getExclusionType() {
		return exclusionType;
	}
	public void setExclusionType(String exclusionType) {
		this.exclusionType = exclusionType;
	}
	public String getActiveDate() {
		return activeDate;
	}
	public void setActiveDate(String activeDate) {
		this.activeDate = activeDate;
	}
	public String getTerminationDate() {
		return terminationDate;
	}
	public void setTerminationDate(String terminationDate) {
		this.terminationDate = terminationDate;
	}
	public String getImportExportCode() {
		return importExportCode;
	}
	public void setImportExportCode(String importExportCode) {
		this.importExportCode = importExportCode;
	}

}
